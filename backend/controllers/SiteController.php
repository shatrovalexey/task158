<?php

namespace backend\controllers;

use common\models\LoginForm;
use Yii;
use yii\filters\{VerbFilter, AccessControl};
use yii\web\{Controller, Response};
use common\models\{User, Url, LogModel};
use yii\data\ActiveDataProvider;

/**
 * Site controller
 */
class SiteController extends Controller
{
    /**
     * {@inheritdoc}
     */
    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::class,
                'rules' => [
                    [
                        'actions' => ['login', 'error', 'report',],
                        'allow' => true,
                    ],
                    [
                        'actions' => ['logout', 'index'],
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::class,
                'actions' => [
                    'logout' => ['post'],
                ],
            ],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function actions()
    {
        return [
            'error' => [
                'class' => \yii\web\ErrorAction::class,
            ],
        ];
    }

	public function actionReport()
	{
		$adp = new ActiveDataProvider([
			'query' => LogModel::getReport()
			, 'pagination' => ['pageSize' => 20,],
		]);

        return $this->render('report', ['adp' => $adp,]);
	}

    /**
     * Displays homepage.
     *
     * @return string
     */
    public function actionIndex()
    {
		return Yii::$app->user->isGuest ? $this->render('index') : $this->redirect(['report',]);
    }

    /**
     * Login action.
     *
     * @return string|Response
     */
    public function actionLogin()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $this->layout = 'blank';

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        }

        $model->password = '';

        return $this->render('login', [
            'model' => $model,
        ]);
    }

    /**
     * Logout action.
     *
     * @return Response
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }
}
